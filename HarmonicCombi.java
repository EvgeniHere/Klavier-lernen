public class HarmonicCombi {
    
    String[] combi = new String[6];
    
    public HarmonicCombi(String a, String b, String c, String d, String e, String f) {
        combi[0] = a;
        combi[1] = b;
        combi[2] = c;
        combi[3] = d;
        combi[4] = e;
        combi[5] = f;
    }
    
    public String[] getHarmonicCombi() {
        return combi;
    }
}
