public class Tonleiter {
    
    String name;
    int[] comb;
    
    public Tonleiter(String name, int[] comb) {
        this.name = name;
        this.comb = comb;
    }
    
    public String getName() {
        return name;
    }
    
    public int[] getCombi() {
        return comb;
    }
}
